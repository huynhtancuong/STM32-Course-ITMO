void init_sine_table() {
    for (size_t i = 0; i < SINE_N_SAMPLE; i++) {
        double angle = 2 * M_PI * i / SINE_N_SAMPLE;
        sine_table[i] = (sin(angle) + 1) * (1 << FIXED_POINT_FRACTIONAL_BITS) / 2;
    }
}
void init_y_sine_digital(double V_max) {
    if (V_max > 3.3) V_max = 3.3;
    if (V_max < 0) V_max = 0.0;

    uint16_t V_max_digital = (V_max / 3.3) * 0xFFF;

    for (size_t i = 0; i < SINE_N_SAMPLE; i++) {
        y_sine_digital[i] = (sine_table[i] * V_max_digital) >> FIXED_POINT_FRACTIONAL_BITS;
    }
}

